# Inserting space between outputs using commas
print("The sum of 2 and 4 is", 2 + 4) 
print("The product of 6 and 8 is", 6 * 8) 
# Custom separator between outputs
print("6", "+", "4", "=", 6 + 4, sep=" " )
print("8", "*", "6", "=", 8 * 6, sep=" ")  
# Inserting text at the end of a print statement using end parameter 
print("4 + 2 =", end=" ") 
print(4 + 2) 
print("2 * 6 =",end=" ") 
print(2 * 6) 